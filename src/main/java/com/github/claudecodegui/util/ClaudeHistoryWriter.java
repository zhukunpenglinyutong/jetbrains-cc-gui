package com.github.claudecodegui.util;

import com.github.claudecodegui.session.ProviderErrorMessageSupport;
import com.github.claudecodegui.util.GsonHolder;
import com.google.gson.Gson;
import com.google.gson.JsonArray;
import com.google.gson.JsonElement;
import com.google.gson.JsonObject;
import com.google.gson.JsonParser;
import com.intellij.openapi.diagnostic.Logger;

import java.io.IOException;
import java.nio.charset.StandardCharsets;
import java.nio.file.Files;
import java.nio.file.Path;
import java.nio.file.Paths;
import java.nio.file.StandardOpenOption;
import java.time.Instant;
import java.util.UUID;

/**
 * Appends synthetic messages (e.g. interruption notices) to Claude CLI JSONL session files.
 * This ensures that messages generated by the plugin (not by the CLI) are persisted
 * and visible when the user reopens the session history.
 */
public final class ClaudeHistoryWriter {

    private static final Logger LOG = Logger.getInstance(ClaudeHistoryWriter.class);
    private static final String HOME_DIR = PlatformUtils.getHomeDirectory();
    private static final Path PROJECTS_DIR = Paths.get(HOME_DIR, ".claude", "projects");
    private static final Gson GSON = GsonHolder.GSON;

    private ClaudeHistoryWriter() {
    }

    /**
     * Append an assistant message to the JSONL session file.
     * The message format matches Claude CLI's JSONL structure to ensure compatibility.
     *
     * @param projectPath the project base path (used to derive the sanitized directory)
     * @param sessionId   the session UUID
     * @param content     the message content (may be an I18N placeholder or plain text)
     */
    public static boolean appendAssistantMessage(String projectPath, String sessionId, String content) {
        return appendAssistantMessage(projectPath, sessionId, content, PROJECTS_DIR);
    }

    static boolean appendAssistantMessage(String projectPath, String sessionId, String content, Path projectsDir) {
        if (projectPath == null || projectPath.isBlank() || sessionId == null || sessionId.isBlank()) {
            LOG.debug("[HistoryWriter] Skipping append: projectPath or sessionId is blank");
            return false;
        }
        if (content == null || content.isBlank()) {
            LOG.debug("[HistoryWriter] Skipping append: content is blank");
            return false;
        }

        try {
            Path sessionFile = resolveSessionFile(projectPath, sessionId, projectsDir);
            if (sessionFile == null) {
                return false;
            }
            if (!Files.isRegularFile(sessionFile)) {
                LOG.debug("[HistoryWriter] Skipping append: session file does not exist: " + sessionFile);
                return false;
            }

            String timestamp = Instant.now().toString();
            String messageUuid = UUID.randomUUID().toString();
            String parentUuid = findLastUuid(sessionFile);

            // Build the assistant message matching Claude CLI JSONL format
            JsonObject line = new JsonObject();
            if (parentUuid != null && !parentUuid.isBlank()) {
                line.addProperty("parentUuid", parentUuid);
            } else {
                line.add("parentUuid", null);
            }
            line.addProperty("isSidechain", false);

            // Message object
            JsonObject message = new JsonObject();
            message.addProperty("id", "msg_" + System.currentTimeMillis() + messageUuid.substring(0, 8));
            message.addProperty("type", "message");
            message.addProperty("role", "assistant");
            message.addProperty("model", "plugin-interrupt");

            // Content as array (matching Claude CLI format)
            JsonArray contentArray = new JsonArray();
            JsonObject textBlock = new JsonObject();
            textBlock.addProperty("type", "text");
            textBlock.addProperty("text", content);
            contentArray.add(textBlock);
            message.add("content", contentArray);

            message.addProperty("stop_reason", "stop");
            message.add("stop_sequence", null);

            // Usage object (minimal)
            JsonObject usage = new JsonObject();
            usage.addProperty("input_tokens", 0);
            usage.addProperty("output_tokens", 0);
            usage.addProperty("cache_creation_input_tokens", 0);
            usage.addProperty("cache_read_input_tokens", 0);
            message.add("usage", usage);

            line.add("message", message);

            // Metadata fields
            line.addProperty("type", "assistant");
            line.addProperty("uuid", messageUuid);
            line.addProperty("timestamp", timestamp);
            line.addProperty("userType", "external");
            line.addProperty("entrypoint", "cli");
            line.addProperty("cwd", projectPath);
            line.addProperty("sessionId", sessionId);

            String jsonLine = GSON.toJson(line);
            Files.writeString(sessionFile, jsonLine + "\n",
                    StandardCharsets.UTF_8,
                    StandardOpenOption.APPEND);

            LOG.info("[HistoryWriter] Appended assistant message to JSONL: " + sessionFile);
            return true;
        } catch (IOException e) {
            LOG.warn("[HistoryWriter] Failed to append message to JSONL: " + e.getMessage());
        } catch (Exception e) {
            LOG.warn("[HistoryWriter] Unexpected error appending to JSONL: " + e.getMessage());
        }
        return false;
    }

    /**
     * Append a provider error as an assistant message with a provider_error content block.
     *
     * @param projectPath the project base path
     * @param sessionId   the session UUID
     * @param summary     short display summary
     * @param details     full error details
     * @param exitCode    optional process exit code
     */
    public static boolean appendProviderError(
            String projectPath,
            String sessionId,
            String summary,
            String details,
            Integer exitCode
    ) {
        return appendProviderError(projectPath, sessionId, summary, details, exitCode, PROJECTS_DIR);
    }

    static boolean appendProviderError(
            String projectPath,
            String sessionId,
            String summary,
            String details,
            Integer exitCode,
            Path projectsDir
    ) {
        if (projectPath == null || projectPath.isBlank() || sessionId == null || sessionId.isBlank()) {
            LOG.debug("[HistoryWriter] Skipping provider error append: projectPath or sessionId is blank");
            return false;
        }
        if (details == null || details.isBlank()) {
            LOG.debug("[HistoryWriter] Skipping provider error append: details is blank");
            return false;
        }

        try {
            Path sessionFile = resolveSessionFile(projectPath, sessionId, projectsDir);
            if (sessionFile == null) {
                return false;
            }
            if (!Files.isRegularFile(sessionFile)) {
                LOG.debug("[HistoryWriter] Skipping provider error append: session file does not exist: " + sessionFile);
                return false;
            }

            String timestamp = Instant.now().toString();
            String messageUuid = UUID.randomUUID().toString();
            String parentUuid = findLastUuid(sessionFile);

            JsonObject line = new JsonObject();
            if (parentUuid != null && !parentUuid.isBlank()) {
                line.addProperty("parentUuid", parentUuid);
            } else {
                line.add("parentUuid", null);
            }
            line.addProperty("isSidechain", false);

            JsonObject message = new JsonObject();
            message.addProperty("id", "msg_" + System.currentTimeMillis() + messageUuid.substring(0, 8));
            message.addProperty("type", "message");
            message.addProperty("role", "assistant");
            message.addProperty("model", "plugin-error");

            JsonArray contentArray = new JsonArray();
            contentArray.add(ProviderErrorMessageSupport.createProviderErrorBlock(
                    "claude",
                    details,
                    summary,
                    exitCode
            ));
            message.add("content", contentArray);
            message.addProperty("stop_reason", "error");
            message.add("stop_sequence", null);

            JsonObject usage = new JsonObject();
            usage.addProperty("input_tokens", 0);
            usage.addProperty("output_tokens", 0);
            usage.addProperty("cache_creation_input_tokens", 0);
            usage.addProperty("cache_read_input_tokens", 0);
            message.add("usage", usage);

            line.add("message", message);
            line.addProperty("type", "assistant");
            line.addProperty("uuid", messageUuid);
            line.addProperty("timestamp", timestamp);
            line.addProperty("userType", "external");
            line.addProperty("entrypoint", "cli");
            line.addProperty("cwd", projectPath);
            line.addProperty("sessionId", sessionId);

            Files.writeString(sessionFile, GSON.toJson(line) + "\n",
                    StandardCharsets.UTF_8,
                    StandardOpenOption.APPEND);

            LOG.info("[HistoryWriter] Appended provider error to JSONL: " + sessionFile);
            return true;
        } catch (IOException e) {
            LOG.warn("[HistoryWriter] Failed to append provider error to JSONL: " + e.getMessage());
        } catch (Exception e) {
            LOG.warn("[HistoryWriter] Unexpected error appending provider error to JSONL: " + e.getMessage());
        }
        return false;
    }

    /**
     * Resolve the JSONL session file path.
     *
     * @param projectPath the project base path
     * @param sessionId   the session UUID
     * @return the resolved path, or null if resolution fails
     */
    private static Path resolveSessionFile(String projectPath, String sessionId, Path projectsDir) {
        try {
            String sanitized = PathUtils.sanitizePath(projectPath);
            if (sanitized.isEmpty()) {
                LOG.warn("[HistoryWriter] Sanitized project path is empty");
                return null;
            }
            Path projectDir = projectsDir.resolve(sanitized);
            return projectDir.resolve(sessionId + ".jsonl");
        } catch (Exception e) {
            LOG.warn("[HistoryWriter] Failed to resolve session file path: " + e.getMessage());
            return null;
        }
    }

    private static String findLastUuid(Path sessionFile) throws IOException {
        String parentUuid = null;
        for (String line : Files.readAllLines(sessionFile, StandardCharsets.UTF_8)) {
            if (line == null || line.isBlank()) {
                continue;
            }
            try {
                JsonElement element = JsonParser.parseString(line);
                if (!element.isJsonObject()) {
                    continue;
                }
                JsonObject object = element.getAsJsonObject();
                if (object.has("uuid") && !object.get("uuid").isJsonNull()) {
                    parentUuid = object.get("uuid").getAsString();
                }
            } catch (Exception ignored) {
                // Ignore malformed historical lines; append after the last valid UUID we can find.
            }
        }
        return parentUuid;
    }
}
